<?php

namespace MusahMusah\LaravelMultipaymentGateways\Traits\Flutterwave;

trait OtpTrait
{
    const OTP_ENDPOINT = '/otps/';

    /**
     * Create an OTP
     *
     * This method allows you to generate an OTP via Flutterwave for any validation.
     *
     * @param array $formParams
     *
     * @return array
     */
    public function createOtp(array $formParams)
    {
        $endpoint = sprintf("%s%s", $this->baseUri, self::OTP_ENDPOINT);

        $otpData = $this->makeRequest(
            method: 'POST',
            requestUrl: $endpoint,
            formParams: $formParams,
            isJsonRequest: true
        );

        return $otpData;
    }

    /**
     * Validate an OTP
     *
     * This method allows you to validate OTPs generated by the /create endpoint.
     *
     * @param string $reference
     * @param array $formParams
     *
     * @return array
     */
    public function verifyOtp(string $reference, array $formParams)
    {
        $endpoint = sprintf("%s%s%s/validate", $this->baseUri, self::OTP_ENDPOINT, $reference);

        $otpData = $this->makeRequest(
            method: 'POST',
            requestUrl: $endpoint,
            formParams: $formParams,
            isJsonRequest: true
        );

        return $otpData;
    }

}
